package com.tj.ex;

public class PersonInfo {
	
	private String name;
	private int age;
	private String gender;
	
	public PersonInfo() {} // 회원
	
	// getter setter 占쏙옙 占쏙옙 占십울옙占싹댐옙.
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	
}
