-- 콤보 박스 /
SELECT GRADE FROM CGRADE;

-- 1. 폰 검색 /
SELECT C.*, G.GRADE, NVL((SELECT HIGH-BUY+1 from CUSTOMER WHERE CNO = C.CNO and GNO != 5), 0) LEVELUP
FROM CUSTOMER C, CGRADE G
WHERE C.GNO = G.GNO AND SUBSTR(CTEL, -4) = '8888';

SELECT C.*, G.GRADE, NVL((SELECT HIGH-BUY+1 from CUSTOMER WHERE CNO = C.CNO and GNO != 5), 0) LEVELUP
FROM CUSTOMER C, CGRADE G
WHERE C.GNO = G.GNO AND CTEL LIKE '%8888';

-- 2.고객이름 검색 /

SELECT C.*, G.GRADE, NVL((SELECT HIGH-BUY+1 from CUSTOMER WHERE CNO = C.CNO and GNO != 5), 0) LEVELUP
FROM CUSTOMER C, CGRADE G
WHERE C.GNO = G.GNO AND CNAME = '손오공';

-- 3.포인트 구매 /
SELECT POINT FROM CUSTOMER WHERE CTEL LIKE '%'||'7777' AND CNAME = '이유리';

UPDATE CUSTOMER SET BUY = (BUY + 500), POINT = (POINT - 500) WHERE CTEL LIKE '%'||'7777' OR CNAME = '이유리';

-- 4.물품구매 /
UPDATE CUSTOMER SET POINT = POINT + (10000 * 0.05), BUY = (BUY + 10000) WHERE CTEL LIKE '%'||'7777' OR CNAME = '이유리';

-- 물건 산 후 구매누적금액에 따라 고객레벨 조정 /
SELECT G.GNO FROM CUSTOMER C, CGRADE G WHERE BUY BETWEEN LOW AND HIGH;

UPDATE CUSTOMER SET GNO = (SELECT G.GNO FROM CUSTOMER C, CGRADE G
                WHERE BUY BETWEEN LOW AND HIGH AND CTEL LIKE '%'||'4444')
            WHERE CTEL LIKE '%'||'4444';

-- 5.등급별 출력 /
SELECT C.*, G.GRADE
FROM CUSTOMER C, CGRADE G
WHERE C.GNO = G.GNO AND G.GRADE = '실버'
ORDER BY CNO;

SELECT C.*, G.GRADE, NVL((SELECT HIGH-BUY+1 from CUSTOMER WHERE CNO = C.CNO and GNO != 5), 0) LEVELUP
FROM CUSTOMER C, CGRADE G
WHERE C.GNO = G.GNO AND G.GRADE = '일반' 
ORDER BY CNO;

