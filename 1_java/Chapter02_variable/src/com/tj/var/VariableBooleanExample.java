package com.tj.var;

public class VariableBooleanExample {

	public static void main(String[] args) { // 논리 데이터 형 변수.
		
		boolean stop = true;
		boolean start = false;
		
		if (stop) {
			System.out.println("중지 합니다.");
		} else {
			System.out.println("시작 합니다.");
		}
		
		if (start) {
			System.out.println("중지 합니다.");
		} else {
			System.out.println("시작 합니다.");
		}

	}

}
